def get_language(request: Any) -> str:
    language = request.cookies.get(LANG_COOKIE_NAME, DEFAULT_LANGUAGE)
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translate(request: Any, key: str, **kwargs: object) -> str:
    language = get_language(request)
    value = TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(
        key,
        TRANSLATIONS["en"].get(key, key),
    )
    return value.format(**kwargs) if kwargs else value
